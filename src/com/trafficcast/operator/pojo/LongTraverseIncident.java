package com.trafficcast.operator.pojo;

public class LongTraverseIncident {

    private long id;

    private String city;

    private double traverseDist;

    public long getId() {
	return id;
    }

    public void setId(long id) {
	this.id = id;
    }

    public String getCity() {
	return city;
    }

    public void setCity(String city) {
	this.city = city;
    }

    public double getTraverseDist() {
	return traverseDist;
    }

    public void setTraverseDist(double traverseDist) {
	this.traverseDist = traverseDist;
    }

}
