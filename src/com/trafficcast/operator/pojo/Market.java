package com.trafficcast.operator.pojo;

public class Market {

    private String state;

    private String city;

    private String market_name;

    private String java_timezone_id;

    private String country;

    public String getState() {
	return state;
    }

    public void setState(String state) {
	this.state = state;
    }

    public String getCity() {
	return city;
    }

    public void setCity(String city) {
	this.city = city;
    }

    public String getMarket_name() {
	return market_name;
    }

    public void setMarket_name(String market_name) {
	this.market_name = market_name;
    }

    public String getJava_timezone_id() {
	return java_timezone_id;
    }

    public void setJava_timezone_id(String java_timezone_id) {
	this.java_timezone_id = java_timezone_id;
    }

    public String getCountry() {
	return country;
    }

    public void setCountry(String country) {
	this.country = country;
    }

}
